def mesh(minimun, maximun, n_div):
    coord = np.linspace(minimun, maximun, n_div + 1)
    coord.shape = (n_div + 1, 1)
    num_nodes = len(coord)
    num_elem = n_div
    connect = np.array([[i, i + 1] for i in range(num_nodes - 1)])
    return coord, connect, num_nodes, num_elem


